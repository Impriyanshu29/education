

//-------------------------------------------- to change color of navbar while scrooling --------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    window.addEventListener('scroll' , () =>
        {
            document.querySelector('nav').classList.toggle
            ('window-scroll' , window.scrollY > 0)
        })

